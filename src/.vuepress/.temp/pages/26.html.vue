<p>昨天我听樊登讲《准备》，特别有感触。很少见樊老师将某本书讲了45分钟以上，这本书讲了78分钟。在讲解视频开始的时候，樊老师介绍说，美国教育工作者黛安娜·塔文纳的这本书有国内人大附中校长等校内教育大伽的推荐，听了之后，果然名符其实，非常值得推荐。这本书的内容很丰富，在此仅列举与本文有关的两点：</p>
